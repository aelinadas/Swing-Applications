    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        // TODO add your handling code here:
        enableFields(false);
        clearFields();
        String searchTerm = searchAircraftTxtField.getText();
        if (searchTerm == null || searchTerm.equals("")) {
            JOptionPane.showMessageDialog(null, "Please enter an aircraft name to search");
            populateTable();
        } else {
            ArrayList<Aircraft> found = airlineDirectory.searchAircraft(searchTerm);
            if (found == null) {
                JOptionPane.showMessageDialog(null, "No aircraft found with provided name");
            } else {
                DefaultTableModel dtm = (DefaultTableModel) aircraftTbl.getModel();
                dtm.setRowCount(0);

                for (Aircraft e : found) {
                    Object row[] = new Object[5];
                    row[0] = e;
                    row[1] = e.getName();
                    row[2] = e.getModel();
                    row[3] = e.isHasEntertainment() ? "Yes" : "No";
                    row[4] = e.isHasWifi() ? "Yes" : "No";
                    dtm.addRow(row);
                }
            }
        }
    }//GEN-LAST:event_searchBtnActionPerformed

    private void confirmBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmBtnActionPerformed
        // TODO add your handling code here:
        try {
            Integer.parseInt(serialNoTxtField.getText());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Please enter valid Serial Number");
            return;
        }
        String name = nameTxtField.getText();
        if (name == null || name.equals("") || !isValidString(name)) {
            JOptionPane.showMessageDialog(null, "Please enter valid name");
            return;
        }
        String model = modelNoTxtField.getText();
        if (model == null || model.equals("") || !isValidString(model)) {
            JOptionPane.showMessageDialog(null, "Please enter valid model");
            return;
        }
        if (name == null || name.equals("")) {
            JOptionPane.showMessageDialog(null, "Please enter name");
            return;
        }
        boolean entertainment = entertainmentCheckBox.isSelected();
        boolean wifi = wifiCheckBox.isSelected();

        if (mode == "new") {
            for (Aircraft e : airlineDirectory.getAircraftDirectory()) {
                if (e.getSerialNo() == Integer.parseInt(serialNoTxtField.getText())) {
                    JOptionPane.showMessageDialog(null, "Aircraft with provided serial number already exists");
                    return;
                }
            }
//            Aircraft aircraft = new Aircraft(Integer.parseInt(serialNoTxtField.getText()), name, model, entertainment, wifi);
//            airlineDirectory.getAircraftDirectory().add(aircraft);
            Aircraft aircraft = airlineDirectory.addAircraft();
            aircraft.setSerialNo(Integer.parseInt(serialNoTxtField.getText()));
            aircraft.setName(name);
            aircraft.setModelNo(model);
            aircraft.setHasEntertainment(entertainment);
            aircraft.setHasWifi(wifi);
            JOptionPane.showMessageDialog(null, "Aircraft added to fleet");
        } else {
            updatedInfo.setSerialNo(Integer.parseInt(serialNoTxtField.getText()));
            updatedInfo.setName(name);
            updatedInfo.setModelNo(model);
            updatedInfo.setHasEntertainment(entertainment);
            updatedInfo.setHasWifi(wifi);
            JOptionPane.showMessageDialog(null, "Aircraft information updated in directory");
        }
        populateTable();
        enableFields(false);
        clearFields();
    }//GEN-LAST:event_confirmBtnActionPerformed

