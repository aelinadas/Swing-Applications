    private void updateAircraftActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateAircraftActionPerformed
        // TODO add your handling code here:
        int selectedRow = aircraftTbl.getSelectedRow();
        if (selectedRow >= 0) {
            mode = "update";
            Aircraft aircraft = (Aircraft) aircraftTbl.getValueAt(selectedRow, 0);
            serialNoTxtField.setText(aircraft.getSerialNo() + "");
            nameTxtField.setText(aircraft.getName() + "");
            modelNoTxtField.setText(aircraft.getModel() + "");
            entertainmentCheckBox.setSelected(aircraft.isHasEntertainment());
            wifiCheckBox.setSelected(aircraft.isHasWifi());
            enableFields(true);
            updatedInfo = aircraft;
        } else {
            enableFields(false);
            clearFields();
            JOptionPane.showMessageDialog(null, "Please select an aircraft to update");
        }
    }//GEN-LAST:event_updateAircraftActionPerformed

    public boolean isValidString(String str) {
        Pattern p = Pattern.compile("^[a-zA-Z0-9]+$");
        Matcher m = p.matcher(str);
        return m.matches();
    }

    public void populateTable() {
        DefaultTableModel dtm = (DefaultTableModel) aircraftTbl.getModel();
        dtm.setRowCount(0);
        ArrayList<Aircraft> temp = airlineDirectory.getAircraftDirectory();
        airlineDirectory.setNoOfAircrafts(temp.size());
        for (Aircraft e : airlineDirectory.getAircraftDirectory()) {

            Object row[] = new Object[5];
            row[0] = e;
            row[1] = e.getName();
            row[2] = e.getModel();
            row[3] = e.isHasEntertainment() ? "Yes" : "No";
            row[4] = e.isHasWifi() ? "Yes" : "No";
            dtm.addRow(row);
        }
    }

