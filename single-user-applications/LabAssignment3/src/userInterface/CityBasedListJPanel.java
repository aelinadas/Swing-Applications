    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        tempArray.clear();
        if (!txtSearch.getText().equals("")) {
          for(Uber uber: uberDirectory.getUberDirectory()){
            if(uber.getCity().equalsIgnoreCase(txtSearch.getText())){
                tempArray.add(uber);
            }
           }
          if (tempArray!=null && !tempArray.isEmpty()) {
            populateTable(tempArray);
          }else{
              JOptionPane.showMessageDialog(null, "Sorry!! No cars available for given city.");
              DefaultTableModel dtm = (DefaultTableModel)tblManage.getModel();
              dtm.setRowCount(0);
          }
        }
        else{
           JOptionPane.showMessageDialog(null, "Please input city name.");
           DefaultTableModel dtm = (DefaultTableModel)tblManage.getModel();
           dtm.setRowCount(0);
        }
    }//GEN-LAST:event_btnSearchActionPerformed

