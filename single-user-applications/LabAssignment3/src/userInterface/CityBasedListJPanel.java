    private void populateTable(ArrayList<Uber> popList){
        DefaultTableModel dtm = (DefaultTableModel)tblManage.getModel();
        dtm.setRowCount(0);
        for(Uber uber: popList){
            Object[] row = new Object[3];
            row[0] = uber;
            row[1] = uber.getCity();
            if (uber.getAvailability()) {
               row[2]="Available";
            } else {
               row[2]="Unavailable";
            }
            
            dtm.addRow(row);
        }
    }


