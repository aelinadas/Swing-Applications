    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        uber.setCarName(txtCarName.getText());
        uber.setModelNumber(txtModelNumber.getText());
        uber.setCity(txtCity.getText());
        uber.setSerialNumber(txtSerialNumber.getText());
        uber.setSeats(Integer.parseInt(txtSeats.getText()));
        uber.setManufactureYear(Integer.parseInt(txtYearOfManufacture.getText()));
        uber.setAvailability(chkAvail.isSelected());
        uber.setMaintenanceCertificate(chkMaintain.isSelected());
        
        btnSave.setEnabled(true);
        btnUpdate.setEnabled(false);
        
        DateFormat date = new SimpleDateFormat("MM/DD/YYYY HH:mm:ss");
        Calendar cal = Calendar.getInstance();
        uberDirectory.setLastModified(date.format(cal.getTime()));
        
        JOptionPane.showMessageDialog(null, "Car updated successfully!!");
    }//GEN-LAST:event_btnSaveActionPerformed

