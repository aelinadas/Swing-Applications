    private void txtCarNameKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCarNameKeyTyped
        // TODO add your handling code here:
        int chk=evt.getKeyCode();
        char ch = evt.getKeyChar();
        if(!Character.isLetter(ch) && (chk!= KeyEvent.VK_BACK_SPACE) && !Character.isWhitespace(ch)){
            evt.consume();
        }
    }//GEN-LAST:event_txtCarNameKeyTyped

