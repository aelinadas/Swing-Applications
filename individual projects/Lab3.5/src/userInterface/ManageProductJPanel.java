    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        // TODO add your handling code here:
        try {
            Integer.parseInt(searchTextField.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Product Number cannot be empty or have alphabet or decimal in it.");
            return;
        }
        Product result = productCatalog.searchProduct(Integer.parseInt(searchTextField.getText()));
        if (result == null) {
            JOptionPane.showMessageDialog(null, "Product Number you entered does not exist", "Information", JOptionPane.INFORMATION_MESSAGE);
        } else {
            ViewJPanel viewJPanel = new ViewJPanel(userProcessContainer, result, productCatalog);
            userProcessContainer.add("ViewJPanel", viewJPanel);
            CardLayout cardLayout = (CardLayout) userProcessContainer.getLayout();
            cardLayout.next(userProcessContainer);
        }
    }//GEN-LAST:event_searchBtnActionPerformed


