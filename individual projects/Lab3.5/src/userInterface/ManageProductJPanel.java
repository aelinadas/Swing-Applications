    public void populate(ArrayList<Product> populateList) {
        DefaultTableModel defaultTableModel = (DefaultTableModel) tblDirectory.getModel();
        defaultTableModel.setRowCount(0);
        for (Product product : populateList) {
            Object[] row = new Object[4];
            row[0] = product;
            row[1] = product.getPrice();
            row[2] = product.getAvailability();
            row[3] = product.getDescription();
            defaultTableModel.addRow(row);
        }
    }

