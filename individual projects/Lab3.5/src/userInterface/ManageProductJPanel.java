    private void ViewBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ViewBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblDirectory.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please Select A Row", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            Product product = (Product) tblDirectory.getValueAt(selectedRow, 0);
            ViewJPanel viewJPanel = new ViewJPanel(userProcessContainer, product, productCatalog);
            userProcessContainer.add("ViewJPanel", viewJPanel);
            CardLayout cardLayout = (CardLayout) userProcessContainer.getLayout();
            cardLayout.next(userProcessContainer);
        }
    }//GEN-LAST:event_ViewBtnActionPerformed

