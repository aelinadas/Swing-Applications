    private void populateTable(ArrayList<Uber> popList){
        DefaultTableModel dtm = (DefaultTableModel)tblManage.getModel();
        dtm.setRowCount(0);
        for(Uber uber: popList){
            Object[] row = new Object[2];
            row[0] = uber;
            row[1] = uber.getModelNumber();
            
            dtm.addRow(row);
        }
    }
